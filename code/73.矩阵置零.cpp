// 给定一个 m x n 的矩阵，如果一个元素为 0 ，则将其所在行和列的所有元素都设为 0 。请使用 原地 算法。

 

// 示例 1：


// 输入：matrix = [[1,1,1],[1,0,1],[1,1,1]]
// 输出：[[1,0,1],[0,0,0],[1,0,1]]
// 示例 2：


// 输入：matrix = [[0,1,2,0],[3,4,5,2],[1,3,1,5]]
// 输出：[[0,0,0,0],[0,4,5,0],[0,3,1,0]]

// 解法一：O(m*n) 时间，O(m+n) 空间 —— 用 set 记录要置零的行列
// 解法二：O(m*n) 时间，O(1) 空间 —— 用矩阵第一行、第一列当标记位

class Solution {
public:
    void setZeroes(vector<vector<int>>& m) {
        //用第一行第一列来标记，空间开销小
        int n = m.size(), mm = m[0].size();
        bool col0 = false;  // 第 0 列是否要整列置零（因 (0,0) 同时表示「第 0 行」的标记）

        // 第一遍：用 m[i][0]、m[0][j] 做标记
        for (int i = 0; i < n; i++) {
            if (m[i][0] == 0) col0 = true;
            for (int j = 1; j < mm; j++) {
                if (m[i][j] == 0) {
                    m[i][0] = 0;
                    m[0][j] = 0;
                }
            }
        }

        // 第二遍：从右下往左上置零，避免覆盖第一行/第一列的标记
        for (int i = n - 1; i >= 0; i--) {
            for (int j = mm - 1; j >= 1; j--) {
                if (m[i][0] == 0 || m[0][j] == 0)
                    m[i][j] = 0;
            }
            if (col0) m[i][0] = 0;
        }
    }
};